public class Score {
    private int score;
    private String color;
    public Score(int score, String color) {
        this.score = score;
        this.color = color;
    }
    public int getScore() {
        return score;
    }
    public void setScore(int score) {
        this.score = score;
    }
    public String getColor() {
        return color;
    }
    public void setColor(String color) {
        this.color = color;
    }
}
